            this.inboxes = inboxes;
            
            // –ü–æ–ª—É—á–∞–µ–º –ª–∏–º–∏—Ç –∏–∑ localStorage –∏–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–µ–º –¥–µ—Ñ–æ–ª—Ç–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
