            this.ui.showToast(`–ü–æ—á—Ç–æ–≤—ã–π —è—â–∏–∫ ${data.emailAddress} –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ —É–¥–∞–ª–µ–Ω —á–µ—Ä–µ–∑ 5 –º–∏–Ω—É—Ç (–ø—É–±–ª–∏—á–Ω—ã–π API)`, 'warning', 5000);
            
