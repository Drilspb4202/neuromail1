            const accountInfo = await this.api.checkAccountStatus();
            this.accountInfo = accountInfo;
            
            // –°–æ—Ö—Ä–∞–Ω—è–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –ª–∏–º–∏—Ç–∞—Ö
