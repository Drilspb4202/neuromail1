        this.ui.onSaveTimeouts = () => this.saveTimeouts();
        this.ui.onSaveAutoDelete = () => this.saveAutoDelete();
        this.ui.onSaveLogging = () => this.saveLogging();
        
        // –°–æ–±—ã—Ç–∏—è –¥–ª—è —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è API –∫–ª—é—á–æ–º –∏ —Ä–µ–∂–∏–º–æ–º
