            let emails = [];
            if (response.content && Array.isArray(response.content)) {
                emails = response.content;
            } else if (Array.isArray(response)) {
                emails = response;
            } else {
                console.warn('–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç –æ—Ç–≤–µ—Ç–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ –ø–∏—Å–µ–º:', response);
            }
            
            // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤ –∫—ç—à
            this.emails[inboxId] = emails;
            
            // –û—Ç–æ–±—Ä–∞–∂–∞–µ–º –ø–∏—Å—å–º–∞
            this.ui.renderEmails(emails, inboxId, this.currentInboxEmail);
            
            // –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å–æ–±—ã—Ç–∏–π –¥–ª—è –Ω–æ–≤—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –ø–∏—Å–µ–º
            this.setupEmailEventHandlers();
            
