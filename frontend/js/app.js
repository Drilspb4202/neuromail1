        this.ui.showToast('–ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ —É–¥–∞–ª–µ–Ω–∏–µ —è—â–∏–∫–∞ –æ—Ç–º–µ–Ω–µ–Ω–æ', 'success');
