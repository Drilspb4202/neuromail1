function fixNavigationItems() {
    // –ò—Å–ø—Ä–∞–≤–ª—è–µ–º –ø–æ–¥—Ö–æ–¥ –∫ —É–ª—É—á—à–µ–Ω–∏—é –Ω–∞–≤–∏–≥–∞—Ü–∏–∏ - –≤–º–µ—Å—Ç–æ –∫–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏—è –±—É–¥–µ–º –º–æ–¥–∏—Ñ–∏—Ü–∏—Ä–æ–≤–∞—Ç—å —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ —ç–ª–µ–º–µ–Ω—Ç—ã
    const navItems = document.querySelectorAll('.nav-item');
    
    navItems.forEach(item => {
        // –£–ª—É—á—à–∞–µ–º —Å—Ç–∏–ª–∏ –¥–ª—è –∞–±—Å–æ–ª—é—Ç–Ω–æ–π —É–≤–µ—Ä–µ–Ω–Ω–æ—Å—Ç–∏
        item.style.display = 'flex';
        item.style.opacity = '1';
        item.style.visibility = 'visible';
        item.style.pointerEvents = 'auto';
        item.style.cursor = 'pointer';
        item.style.position = 'relative';
        item.style.zIndex = '20';
        
        // –£–±–µ–∂–¥–∞–µ–º—Å—è, —á—Ç–æ —Ç–µ–∫—Å—Ç—ã –æ—Ç–æ–±—Ä–∞–∂–∞—é—Ç—Å—è
        const textSpan = item.querySelector('span');
        if (textSpan) {
            textSpan.style.display = 'inline-block';
            textSpan.style.opacity = '1';
            textSpan.style.visibility = 'visible';
            textSpan.style.pointerEvents = 'none';
        }
        
        // –û—á–∏—â–∞–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ —Å–æ–±—ã—Ç–∏—è –∏ –¥–æ–±–∞–≤–ª—è–µ–º –Ω–æ–≤—ã–π –æ–±—Ä–∞–±–æ—Ç—á–∏–∫
        const newItem = item.cloneNode(true);
        newItem.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            
            const target = this.getAttribute('data-target');
            if (target) {
                // –î–µ–ª–∞–µ–º –≤—Å–µ —Å–µ–∫—Ü–∏–∏ –Ω–µ–∞–∫—Ç–∏–≤–Ω—ã–º–∏
                document.querySelectorAll('.content-section').forEach(section => {
                    section.classList.remove('active');
                });
                
                // –ê–∫—Ç–∏–≤–∏—Ä—É–µ–º —Ü–µ–ª–µ–≤—É—é —Å–µ–∫—Ü–∏—é
                const targetSection = document.getElementById(target);
                if (targetSection) {
                    targetSection.classList.add('active');
                }
                
                // –û–±–Ω–æ–≤–ª—è–µ–º –∞–∫—Ç–∏–≤–Ω—ã–π –∫–ª–∞—Å—Å –≤ –Ω–∞–≤–∏–≥–∞—Ü–∏–∏
                document.querySelectorAll('.nav-item').forEach(navItem => {
                    navItem.classList.remove('active');
                });
                this.classList.add('active');
                
                console.log(`Navigated to: ${target}`);
            }
        });
        
        // –ë–µ–∑–æ–ø–∞—Å–Ω–æ –∑–∞–º–µ–Ω—è–µ–º —ç–ª–µ–º–µ–Ω—Ç
        if (item.parentNode) {
            item.parentNode.replaceChild(newItem, item);
        }
    });
    
    // –î–µ–ª–∞–µ–º –Ω–∞–≤–∏–≥–∞—Ü–∏—é –ø—Ä–æ–∫—Ä—É—á–∏–≤–∞–µ–º–æ–π
    const appNav = document.querySelector('.app-nav');
    if (appNav) {
        appNav.style.overflowX = 'auto';
        appNav.style.webkitOverflowScrolling = 'touch';
        appNav.style.justifyContent = 'flex-start';
    }
}

